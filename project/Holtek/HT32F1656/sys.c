#include "sys.h"
/*******************************************************************************************************
* 函 数 名：SysInit
* 功能说明：上电初始化
* 形    参：void
* 返 回 值：无
********************************************************************************************************/
void SysInit(void)
{
	Delay_Init();
 	USART0_Init(115200);			// 串口0初始化函数，波特率115200
	UART0_Init(115200);
  USART1_Init(2400);
	UART1_Init(9600);
	HMISendStart();
 	get_dianneng();
	HMI_getwifi();	

  Att7022Init();					// ATT7022初始化函数
}


